#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (*s)
	{
		s++;
		i++;
	}
	while (i > 0 && *s != c)
	{
		i--;
		s--;
	}
	return ((*s == c ? (char *)s : NULL));
}
